function Conditionalstatement (input){
	if (input > 25){
		alert("input is greater than 25.");
	}  else if (input <= 25){
		console.log("input is less than 25.");
	}  else {
		alert("the number is 25.");
	}

}

//test values
Conditionalstatement(30);
Conditionalstatement(25);
Conditionalstatement(5);